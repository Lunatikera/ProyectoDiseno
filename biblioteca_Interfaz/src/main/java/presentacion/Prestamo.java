    private void llenarCampos() {

        LibroDTO libro;
        try {
            libro = libroBO.consultarLibro(Guardado.getIsbn());
        
        ImageIcon icon = new ImageIcon(libro.getPortadaDTO());
        Image scaledImage = icon.getImage().getScaledInstance(195, 242, Image.SCALE_SMOOTH);
        portada.setIcon(new ImageIcon(scaledImage));
        txtTitulo.setText(libro.getTituloDTO());
        autor.setText(libro.getAutorDTO());
        genero.setText(libro.getGeneroDTO());
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Informaci坦n", JOptionPane.ERROR_MESSAGE);
        }
    }
