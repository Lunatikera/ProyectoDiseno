    private void llenarCampos() {
        LibroDTO libro;
        try {
            libro = libroISBN.consultarLibro(Guardado.getIsbn());

            ImageIcon icon = new ImageIcon(libro.getPortadaDTO());
            Image scaledImage = icon.getImage().getScaledInstance(195, 242, Image.SCALE_SMOOTH);
            portada.setIcon(new ImageIcon(scaledImage));
            txtTitulo.setText(libro.getTituloDTO());
            txtAutor.setText(libro.getAutorDTO());
            txtGenero.setText(libro.getGeneroDTO());
        } catch (FlitroException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Informaci√≥n", JOptionPane.ERROR_MESSAGE);
        }
    }
